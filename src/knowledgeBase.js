/**
 * Определяет подходящие домены по ключевым словам в тексте
 * @param {string} text — текст пользователя
 * @returns {string[]} — массив ключей доменов, отсортированных по релевантности (макс 3)
 */
export function detectDomain(text) {
  const lower = (text || '').toLowerCase();
  const scores = {};

  for (const [key, domain] of Object.entries(KNOWLEDGE_BASE)) {
    let score = 0;
    for (const kw of domain.keywords) {
      if (lower.includes(kw.toLowerCase())) score++;
    }
    if (score > 0) scores[key] = score;
  }

  return Object.entries(scores)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 3)
    .map(([key]) => key);
}


/**
 * Собирает контекст базы знаний для промпта
 * ОБНОВЛЕНО v2.0: принимает cardType и userLevel для оптимизации токенов
 *
 * @param {string[]} domainKeys — ключи доменов из detectDomain()
 * @param {object} options — опциональные параметры
 * @param {string} options.cardType — тип карточки: 'task' | 'track' | 'recommendation' | 'diagnostic'
 * @param {string} options.userLevel — уровень пользователя: 'junior' | 'middle' | 'senior' | 'unknown'
 * @returns {string} — текстовый контекст для подстановки в промпт
 */
export function buildKnowledgeContext(domainKeys, options = {}) {
  const { cardType = null, userLevel = 'unknown' } = options;
  const parts = [];

  for (const key of domainKeys) {
    const domain = KNOWLEDGE_BASE[key];
    if (!domain) continue;

    parts.push(`\n=== СФЕРА: ${domain.name} ===`);

    // Фреймворки — всегда включаем
    parts.push('\nФреймворки:');
    for (const f of domain.frameworks) {
      parts.push(`- ${f.name}: ${f.desc}. Применение: ${f.how}`);
    }

    // Методики — всегда включаем
    parts.push('\nМетодики:');
    for (const m of domain.methodologies) {
      parts.push(`- ${m.name}: ${m.desc}`);
      parts.push(`  Шаги: ${m.steps.join(' → ')}`);
    }

    // Инструменты — всегда (но для диагностики — сокращённо)
    if (cardType !== 'diagnostic') {
      parts.push('\nИнструменты:');
      for (const t of domain.tools) {
        parts.push(`- ${t.name} (${t.url}): ${t.desc}`);
      }
    }

    // Метрики — всегда
    parts.push('\nМетрики: ' + domain.metrics.join(', '));

    // Шаблоны заданий — для задач и треков
    if (!cardType || cardType === 'task' || cardType === 'track') {
      parts.push('\nШаблоны заданий:');
      for (const t of domain.task_templates) {
        parts.push(`- ${t.title}: ${t.goal}`);
        parts.push(`  Шаги: ${t.detailed_steps.join(' | ')}`);
        parts.push(`  Метрики: ${t.metrics.join(', ')}`);
      }
    }

    // ── ЭКСПЕРТНЫЕ СЛОИ (v2.0) ──

    // Калибровка уровня — для диагностики и треков
    if (domain.expertise_levels && (!cardType || cardType === 'diagnostic' || cardType === 'track')) {
      parts.push('\nУровни экспертизы:');
      for (const [level, data] of Object.entries(domain.expertise_levels)) {
        parts.push(`\n  [${level.toUpperCase()}] ${data.title}`);
        parts.push(`  Знает: ${data.knows.join('; ')}`);
        parts.push(`  Не знает: ${data.doesnt_know.join('; ')}`);
        parts.push(`  Фокус: ${data.focus}`);
        parts.push(`  Типичные ошибки: ${data.typical_mistakes.join('; ')}`);
      }
    }

    // Анти-паттерны — для задач и диагностики, фильтр по уровню
    if (domain.anti_patterns && (!cardType || cardType === 'task' || cardType === 'diagnostic')) {
      const relevantAP = userLevel !== 'unknown'
        ? domain.anti_patterns.filter(ap => ap.level === userLevel || ap.level === 'junior')
        : domain.anti_patterns;

      parts.push('\nАнти-паттерны (типичные ошибки):');
      for (const ap of relevantAP) {
        parts.push(`- ${ap.name}: ${ap.what}. Почему плохо: ${ap.why_bad}. Как исправить: ${ap.how_to_fix}`);
      }
    }

    // Сигналы успеха — для диагностики и треков
    if (domain.success_signals && (!cardType || cardType === 'diagnostic' || cardType === 'track')) {
      parts.push('\nСигналы успеха:');
      for (const s of domain.success_signals) {
        parts.push(`- [${s.level}] ${s.signal}: ${s.what_it_means}`);
      }
    }

    // Продвинутые техники — для задач и рекомендаций middle+
    if (domain.advanced_techniques && (!cardType || cardType === 'task' || cardType === 'recommendation')) {
      const relevantAT = userLevel === 'junior'
        ? [] // джуниорам не показываем advanced
        : domain.advanced_techniques;

      if (relevantAT.length > 0) {
        parts.push('\nПродвинутые техники:');
        for (const at of relevantAT) {
          parts.push(`- ${at.name} [${at.level}+]: ${at.desc}`);
          parts.push(`  Когда использовать: ${at.when_to_use}`);
          parts.push(`  Шаги: ${at.steps.join(' → ')}`);
        }
      }
    }

    // Связи фреймворков — для рекомендаций и треков
    if (domain.framework_connections && (!cardType || cardType === 'recommendation' || cardType === 'track')) {
      parts.push('\nСвязи между фреймворками:');
      for (const fc of domain.framework_connections) {
        parts.push(`- ${fc.from} → ${fc.to}: ${fc.relation}`);
      }
    }

    // Ловушки реального мира — для задач middle+
    if (domain.real_world_traps && (!cardType || cardType === 'task')) {
      const relevantTraps = userLevel === 'junior'
        ? domain.real_world_traps.filter(t => t.level === 'junior')
        : domain.real_world_traps;

      if (relevantTraps.length > 0) {
        parts.push('\nЛовушки реального мира:');
        for (const trap of relevantTraps) {
          parts.push(`- Ситуация: ${trap.situation}`);
          parts.push(`  Почему сложно: ${trap.why_tricky}`);
          parts.push(`  Как действовать: ${trap.how_to_handle}`);
        }
      }
    }

    // Бенчмарки — для диагностики и аналитических задач
    if (domain.industry_benchmarks && (!cardType || cardType === 'diagnostic' || cardType === 'task')) {
      parts.push('\nОтраслевые бенчмарки:');
      for (const b of domain.industry_benchmarks) {
        parts.push(`- ${b.metric}: плохо ${b.bad} | норма ${b.ok} | хорошо ${b.good} | отлично ${b.great} (${b.context})`);
      }
    }
  }

  return parts.join('\n');
}
